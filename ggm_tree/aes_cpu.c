/* https://www.samiam.org/galois.html */
unsigned char galois_mul(unsigned char a, unsigned char b) {
	int s;
	int q;
	int z = 0;
	s = ltable[a] + ltable[b];
	s %= 255;
	s = atable[s];
	q = s;
	if(a == 0) {
		s = z;
	}
	else {
		s = q;
	}
	if(b == 0) {
		s = z;
	} 
	else {
		q = z;
	}
	return s;
}

static void byteSub(unsigned char state[][STATE_DIM]) {
    for (int i = 0; i < STATE_DIM; ++i) {
        for (int j = 0; j < STATE_DIM; ++j) {
            state[i][j] = sbox[state[i][j]];
        }
    }
}

static void shiftRows(unsigned char state[][STATE_DIM]) {
    unsigned char temp;

    // Row 1
    temp        = state[1][0];
    state[1][0] = state[1][1];
    state[1][1] = state[1][2];
    state[1][2] = state[1][3];
    state[1][3] = temp;

    // Row 2
    temp        = state[2][0];
    state[2][0] = state[2][2];
    state[2][2] = temp;
    temp        = state[2][1];
    state[2][1] = state[2][3];
    state[2][3] = temp;

    // Row 3
    temp        = state[3][0];
    state[3][0] = state[3][3];
    state[3][3] = state[3][2];
    state[3][2] = state[3][1];
    state[3][1] = temp;
}

static void mixColumns(unsigned char state[][STATE_DIM]) {
    unsigned char b[STATE_DIM];

    for (int i = 0; i < STATE_DIM; ++i) {
        for (int j = 0; j < STATE_DIM; ++j) {
            b[j] = state[j][i];
        }

        state[0][i] = galois_mul(b[0], 2) ^ galois_mul(b[1], 3) ^ b[2] ^ b[3];
        state[1][i] = b[0] ^ galois_mul(b[1], 2) ^ galois_mul(b[2], 3) ^ b[3];
        state[2][i] = b[0] ^ b[1] ^ galois_mul(b[2], 2) ^ galois_mul(b[3], 3);
        state[3][i] = galois_mul(b[0], 3) ^ b[1] ^ b[2] ^ galois_mul(b[3], 2);
    }
}

static void keyAddition(unsigned char state[][STATE_DIM], unsigned char* key) {
    for (int i = 0; i < STATE_DIM; ++i) {
        for (int j = 0; j < STATE_DIM; ++j) {
            state[i][j] = state[i][j] ^ key[i + STATE_DIM*j];
        }
    }
}

static void gFunc(unsigned char* word, int iter) {
    unsigned char temp = word[0];
    word[0] = sbox[word[1]] ^ rcon[iter];
    word[1] = sbox[word[2]];
    word[2] = sbox[word[3]];
    word[3] = sbox[temp];
}

static void makeKeySchedule(unsigned char* keySchedule, unsigned char* key) {
    for (int i = 0; i < BLOCKSIZE; ++i) {
        keySchedule[i] = key[i];
    }

    for (int i = 1; i <= NUM_ROUNDS; ++i) {
        unsigned char temp[WORDSIZE];

        for (int j = 0; j < WORDSIZE; ++j) {
            temp[j] = keySchedule[i*BLOCKSIZE - WORDSIZE + j];
        }
        
        gFunc(temp, i);
        for (int j = 0; j < WORDSIZE; ++j) {
            keySchedule[i*BLOCKSIZE + j] = keySchedule[(i-1)*BLOCKSIZE + j] ^ temp[j];
        }

        for (int j = WORDSIZE; j < BLOCKSIZE; ++j) {
            keySchedule[i*BLOCKSIZE + j] = keySchedule[(i-1)*BLOCKSIZE + j] ^ keySchedule[i*BLOCKSIZE + j - WORDSIZE];
        }
    }
}

// 128 bit single block
void myAES_encrypt (const unsigned char *in, unsigned char *out, unsigned char *key) {
                   
    // my CPU implementation here
    // make matrix and do initial key addition 
    unsigned char state[STATE_DIM][STATE_DIM];
    for (int i = 0; i < STATE_DIM; ++i) {
        for (int j = 0; j < STATE_DIM; ++j) {
            state[i][j] = in[i + STATE_DIM*j] ^ key[i + STATE_DIM*j];
        }
    }

    // key schedule
    unsigned char keySchedule[BLOCKSIZE*(NUM_ROUNDS+1)];
    makeKeySchedule(keySchedule, key);

    for (int i = 1; i < NUM_ROUNDS; ++i) {
        byteSub(state);
        shiftRows(state);
        mixColumns(state);
        keyAddition(state, keySchedule + (BLOCKSIZE * i));
    }

    byteSub(state);
    shiftRows(state);
    keyAddition(state, keySchedule + (BLOCKSIZE * NUM_ROUNDS));

    for (int i = 0; i < STATE_DIM; ++i) {
        for (int j = 0; j < STATE_DIM; ++j) {
            out[i + STATE_DIM*j] = state[i][j];
        }
    }
}

